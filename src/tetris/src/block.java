package tetris.src;

class Block
{
	int xIndex,yIndex;
	
	public int getXIndex()
	{
		return xIndex;
	}
	
	public int getYIndex()
	{
		return yIndex;
	}
	
	public void setXIndex(int x)
	{
		xIndex = x;
	}
	
	public void setYIndex(int y)
	{
		yIndex = y;
	}
}